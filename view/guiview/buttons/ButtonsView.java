package view.guiview.buttons;

import java.awt.event.ActionListener;


/**
 * This interface consists of the all the methods
 * are implemented by the costBasisOption, createPortfolioOptionsFlexible,
 * FlexibleAllOptions, PortfolioPerformanceAll and UpdatePortfolio class to
 * perform an action when a button is clicked.
 **/
public interface ButtonsView {


  /**
   * This method is handling all action events such as when
   * the user clicks on a Button.
   *
   * @param listener ActionListener, It contains the listner
   */

  void addActionListener(ActionListener listener);


  /**
   * This method resets the focus on the component created
   * on the screen when an Action is performed.
   */

  void resetFocus();


}
